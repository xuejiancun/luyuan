package com.luyuan.mobile.model;


public class WarehousePurchaseVoucherDetailInfo {

	private String itemCode = "";
	private String itemName = "";
	private String itemID = "";
	private String QTY = "";
	private String ExamineQTY = "";
	private String BADQTY = "";
	private String ActualQTY = "";

	public String getitemCode() {
		return itemCode;
	}

	public void setitemCode(String itemCode) {
		this.itemCode = itemCode;
	}

	public String getitemName() {
		return itemName;
	}

	public void setitemName(String itemName) {
		this.itemName = itemName;
	}

	public String getitemID() {
		return itemID;
	}

	public void setitemID(String itemID) {
		this.itemID = itemID;
	}

	public String getQTY() {
		return QTY;
	}

	public void setQTY(String QTY) {
		this.QTY = QTY;
	}

	public String getExamineQTY() {
		return ExamineQTY;
	}

	public void setExamineQTY(String ExamineQTY) {
		this.ExamineQTY = ExamineQTY;
	}

	public String getBADQTY() {
		return BADQTY;
	}

	public void setBADQTY(String BADQTY) {
		this.BADQTY = BADQTY;
	}

	public String getActualQTY() {
		return ActualQTY;
	}

	public void setActualQTY(String ActualQTY) {
		this.ActualQTY = ActualQTY;
	}
}
